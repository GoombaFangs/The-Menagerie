#include "rocky.h"

void print_rocky(int variant) 
{
	switch (variant)
	{
	case 0:
		printf(
			"                   [#####]\n"
			"                   [#######]\n"
			"                    [#####]\n"
			"                     [###]\n"
		);
		break;

	case 1:
		printf(
			"                    [#########]\n"
			"                   [## ##### ##]\n"
			"                   [#####8#####]\n"
			"                    [####^####]\n"
			"                     |#|  |#|\n"
		);
		break;

	case 2:
		printf(
			"                    [#########]\n"
			"                   [#  o   o  #]\n"
			"                   [#    8    #]\n"
			"                    [####^####]\n"
			"                      ||   ||\n"
		);
		break;

	case 3:
		printf(
			"                     _________\n"
			"                   / @#@#@#@#@\\\n"
			"                  |  0 ### 0  |\n"
			"                  |@@@@ ^  @@@|\n"
			"                   \\_________/\n"
		);
		break;

	case 4:
		printf(
			"                     ________\n"
			"                    /  o  o  \\\n"
			"                   |   ____   |\n"
			"                    \\_/    \\_/\n"
			"                     |      |\n"
			"                     |  ||  |\n"
		);
		break;

	case 5:
		printf(
			"                    [=========]\n"
			"                   [=  o   o  =]\n"
			"                   [=    -    =]\n"
			"                    [=========]\n"
			"                      ||   ||\n"
		);
		break;

	case 6:
		printf(
			"                    [#########]\n"
			"                   [##   o   ##]\n"
			"                   [##  /_\\  ##]\n"
			"                    [####^####]\n"
		);
		break;

	case 7:
		printf(
			"                    [#########]\n"
			"                   [#  o   o  #]\n"
			"                   [#   ===   #]\n"
			"                    [##-----##]\n"
			"                     ||     ||\n"
			"                    /__\\   /__\\\n"
		);
		break;

	case 8:
		printf(
			"                      [#####]\n"
			"                     [## o ##]\n"
			"                      [#####]\n"
			"                        ||\n"
			"                      [#####]\n"
			"                     [## ^ ##]\n"
			"                      [#####]\n"
		);
		break;

	case 9:
		printf(
			"                      +---------+\n"
			"                      | ####### |\n"
			"                      | # o o # |\n"
			"                      | #  ^  # |\n"
			"                      | ####### |\n"
			"                      +--+---+--+\n"
			"                      |  |   |  |\n"
			"                      |  |   |  |\n"
			"                      +--+   +--+\n"
		);
		break;
	}
}